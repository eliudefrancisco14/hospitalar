                        <b>VISTO DE TURISMO</b> <br><br>
                        É concedido pelas Missões diplomáticas e consulares angolanas ao cidadão estrangeiro que
                        pretenda entrar na República de Angola, em visita de carácter recreativo, desportivo ou
                        cultural.
                        O visto de turismo deve ser utilizado no prazo de 60 dias subsequentes à data da sua concessão,
                        é
                        válido para uma ou múltiplas entradas e permite a permanência em Angola por um período de até
                        30 dias, sendo prorrogável uma única vez, por igual período.
                        O visto de turismo não permite ao seu titular a fixação de residência em Angola, nem o exercício
                        de qualquer actividade remunerada.<br><br>



                        <b>VISTO DE CURTA DURAÇÃO</b> <br><br>
                        É concedido pelas Missões diplomáticas e consulares angolanas ao cidadão estrangeiro que, por
                        razões de urgência, tenha necessidade de entrar em Angola.
                        O visto de curta duração, deve ser utilizado no prazo de 72 horas subsequentes a data da sua
                        concessão, permite ao seu titular a permanência até sete (7) dias e é prorrogável por igual
                        período
                        de tempo. III.
                        O visto de curta duração não permite ao seu titular a fixação de residência em Angola, nem o
                        exercício de qualquer actividade remunerada.<br><br>

                        <b>VISTO ORDINÁRIO</b> <br><br>
                        É concedido pelas Missões diplomáticas e consulares angolanas ao cidadão estrangeiro e destina-
                        se a permitir a entrada em Angola por razões familiares e prospecção de negócios.
                        O visto ordinário deve ser utilizado no prazo de 60 dias, subsequentes à data da sua concessão e
                        permite o seu titular a permanência até 30 dias e pode ser prorrogável duas vezes, por igual
                        período de tempo.
                        O visto ordinário não permite ao seu titular a fixação de residência em Angola, nem o exercício
                        de
                        qualquer actividade remunerada.
                        <br><br>




                        <b>VISTO DE FRONTEIRA</b> <br><br>
                        É concedido pela Autoridade Migratória nos postos de Fronteira e destina-se a permitir a entrada
                        em território nacional ao cidadão estrangeiro que por razões imprevistas e devidamente
                        fundamentadas não tenha solicitado visto num Posto Consular e venha, nomeadamente, para
                        proceder à montagem de equipamentos, para prestar assistência técnica pós venda, para
                        desenvolver outra actividade semelhante.
                        O Visto de Fronteira é valido para uma entrada e permite a permanência do beneficiário em
                        território nacional por um período de 15 dias, não prorrogáveis.
                        O Visto de fronteira não permite ao seu titular afixação de residência no País, nem o exercício
                        de
                        qualquer actividade remunerada.
                        <br><br>


