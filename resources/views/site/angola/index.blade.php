                <h2 class="mt-5 pt-5 mb-3">Os Principais Rios </h2>

                <p class="text-justify">
                    O principal rio de Angola é o Kwanza, que dá o nome a moeda nacional com 1000 Km de
                    longitude,
                    mas
                    apenas 240 Km são navegáveis. De seguida, o Kubango com 975 Km, depois o Cunene com 800
                    Km e por
                    fim, na lista dos quatro principais do país, o Zaire com 150 Km de longitude, sendo este
                    último,
                    todo ele navegável.

                    Os rios angolanos oferecem oportunidades para a implementação de negócios de interesse
                    turístico
                    ou
                    mistos do tipo comércio-turismo ou ainda a prática do eco-turismo.
                </p>


                <h2 class="mt-5 pt-5 mb-3">Recursos Minerais</h2>

                Angola é um país potencialmente rico em recursos minerais. Estima-se que o seu sub-solo albergue 35
                dos
                45 mais importantes do comércio mundial entre os quais se destacam o petróleo, gás natural,
                diamantes,
                fosfatos, substâncias betuminosas, ferro, cobre, magnésio, ouro e rochas ornamentais, etc.

                <h2 class="mt-5 pt-5 mb-3"> Clima</h2>

                <p class="text-justify">
                    O Clima em Angola tem duas estações: a das chuvas, período mais quente que ocorre entre os meses
                    de Setembro a Maio, e a do Cacimbo. A do Cacimbo ou Seca é menos quente e vai de Maio a
                    Setembro. <br>

                    O país possui uma situação geográfica peculiar, por estar na zona inter-tropical e sub-tropical
                    do hemisfério Sul, ser próximo ao mar, e pelas especificidades do seu relevo, divide-se em duas
                    regiões climáticas distintas: <br>

                <ul class="text-justify">
                    <li class="mt-4">
                        A Região Litoral - com humidade relativa média anual de 30% e temperatura média superior
                        aos
                        23°C
                    </li>
                    <li class="mt-4">
                        A Região do Interior, sub-dividida em Zona Norte, com elevadas quedas pluviométricas e
                        temperaturas altas , zona de Altitude que abrange as regiões planálticas centrais com uma
                        estação Seca de temperaturas baixas e a Zona Sudoeste, semi-árida em conseqüência da
                        proximidade do
                        deserto do Namibe, extensão do deserto do Kalahari, sujeita a grandes massas de ar tropical
                        continental
                    </li>
                </ul>
                <b> As Temperaturas Médias do país são: 27°C máxima e 17°C mínima</b> <br> <br>

                A esta diversidade climática corresponde um potencial turístico representado por um património
                natural riquíssimo em flora e fauna diversificada, possibilitando a prática de todo tipo de
                actividades de lazer, hobbies e aventuras.
                </p>

