                        <p class="text-justify">
                            O cidadão João Manuel Gonçalves Lourenço nasceu no dia 5 de Março de 1954, na cidade do
                            Lobito, província de Benguela, filho de Sequeira João Lourenço, enfermeiro, e de Josefa
                            Gonçalves Cipriano Lourenço, costureira, ambos falecidos. É casado com Ana Afonso Dias
                            Lourenço e pai de seis filhos. <br><br>

                            Fez os estudos primários e secundários na província do Bié e também na cidade capital,
                            na antiga Escola Industrial de Luanda e no Instituto Industrial de Luanda. De 1978 a
                            1982, recebeu na então União Soviética formação militar, tendo-se especializado em
                            artilharia pesada. Nesse país, obteve igualmente o grau académico de Mestre em Ciências
                            Históricas. Tem o domínio fluente do inglês, do russo e do espanhol.<br> <br>

                            Participou a partir de Agosto de 1974 na luta de Libertação Nacional, conduzida pelo
                            MPLA, tendo feito a sua primeira instrução político-militar no Centro de Instrução
                            Revolucionária – CIR ‘Kalunga’ – no Congo Brazzaville. Integrou o primeiro grupo de
                            combatentes do MPLAqui em 1974 entrou em território nacional, via Miconge, em direcção
                            à cidade de Cabinda. <br><br>

                            Nas vésperas da Independência, participou em combates em Ntó-Iema, na província de
                            Cabinda, e em outras localidades, contra a invasão do exército zairense que pretendia
                            ocupar o território daquela província de Angola. Exerceu também as funções de Comissário
                            Político das então Forças Armadas Populares de Libertação de Angola (FAPLA) em diversos
                            escalões, incluindo a de Comissário Político da 2ª Região Político-Militar de Cabinda.
                            <br><br>

                            Durante a década de 80 do século XX, participou em várias operações militares no centro
                            do país, nomeadamente nas províncias do Cuanza Sul, Huambo e Bié. Em 1983 exerceu a
                            Presidência do Conselho Militar Regional da 3ª Região Político-Militar. Nas Forças
                            Armadas, desempenhou ainda as funções de Chefe da Direcção Política das FAPLA, de 1989 a
                            1990. Actualmente é General-de-Três-Estrelas, na reserva, das Forças Armadas Angolanas
                            (FAA).<br><br>

                            Integra o Comité Central do MPLA desde 1985, é membro do seu Bureau Político desde 1990
                            e, na sequência do VII Congresso Ordinário do MPLA, realizado em 2016, foi eleito
                            Vice-Presidente do MPLA. Anteriormente, já tinha desempenhado as funções de 1º
                            Secretário do Bureau Político e também Secretário do Bureau Político do MPLA para a
                            Informação e para a Esfera Económica e Social.<br><br>

                            No domínio das funções governativas, foi nomeado em 1983, aos 29 anos de idade, para
                            exercer o cargo de Comissário Provincial do Moxico, equivalente ao cargo actual de
                            Governador de Província. Foi posteriormente, de 1986 a 1989, Comissário Provincial de
                            Benguela. Em 2014, por Decreto Presidencial, foi nomeado Ministro da Defesa Nacional,
                            cargo que exerceu até ao início da campanha eleitoral de 2017.<br><br>

                            Ao nível parlamentar, o cidadão João Lourenço foi Presidente do Grupo Parlamentar do
                            MPLA, de 1991 a 1998, e posteriormente Presidente da Comissão Constitucional da
                            Assembleia Nacional, tendo desempenhado as funções de 1º Vice-Presidente da Assembleia
                            Nacional, de 2003 a 2014.<br><br>

                            O cidadão João Lourenço tem como passatempo a leitura, o xadrez, a equitação e é um
                            apaixonado pelas novas Tecnologias de Informação.
