                <p class="text-justify">
                    A riqueza cultural de Angola manifesta-se em diferentes áreas. No artesanato, destaca-se a
                    variedade de materiais utilizados. Através de estatuetas em madeira, instrumentos musicais,
                    máscaras para danças rituais, objectos de uso comum, ricamente ornamentados, pinturas a óleo e
                    areia, é comprovada a qualidade artística angolana, patente em museus, galerias de arte e
                    feiras. Associado às festas tradicionais promovidas por etnias locais está também um grande
                    valor cultural. A presença constante da dança no quotidiano é produto de um contexto cultural
                    apelativo para a interiorização de estruturas rítmicas desde cedo. Iniciando-se pelo estreito
                    contacto da criança com os movimentos da mãe (às costas da qual é transportada), esta ligação é
                    fortalecida através da participação dos jovens nas diferentes celebrações sociais (os jovens são
                    os que mais se envolvem), onde a dança se revela determinante enquanto factor de integração e
                    preservação da identidade e do sentimento comunitário. <br><br>

